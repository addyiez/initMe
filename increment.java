public class increment {
    public static void main(String args []){
        int a=10;
        int b=++a; //pre increment
        System.out.println(a);
        System.out.println(b);

        int c=10;
        int d=c++; //post increment
        System.out.println("\n"+c);
        System.out.println(d);

    }
    
}
