public class switchCase {

    public static void main(String args[]){

        int number=3;
        switch(number){
            case 1: System.out.println("samosa");
            break;
            case 2: System.out.println("pizza");
            break;
            case 3: System.out.println("burger");
            break;
            default : System.out.println("nothing will serve");
        }
    }
    
}
